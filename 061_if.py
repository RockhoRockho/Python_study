#     3의 배수이면 "3의 배수입니다" 라고 출력해주고
#     3의 배수가 아니면 "홀수이지만 3의 배수는 아닙니다" 라고 출력

# 짝수 이면 "짝수입니다" 라고 출력하고
#     6의 배수면 "6의 배수입니다" 라고 출력하고
#     6의 배수가 아니면 "짝수이지만 6의 배수는 아닙니다" 라고 출력

#  예]
#  7 의 경우

#  출력]
#  홀수입니다
#  홀수이지만 3의 배수는 아닙니다

n = 3

if n % 2 == 0:
    print('짝수입니다')
    if n % 6 == 0:
        print('6의 배수입니다')
    else:
        print('짝수이지만 6의 배수는 아닙니다')
else:
    print('홀수입니다')
    if n % 3 == 0:
        print('3의 배수입니다')
    else:
        print('홀수이지만 3의 배수는 아닙니다')

