# 등급은
# 90점 이상은 A
# 80점 이상은 B
# 70점 이상은 C
# 60점 이상은 D
# 그 이하는 F

# 예1]
# 국,영,수 입력:78 89 67
# 총점: 234
# 평균: 78.0
# 등급: C

# 예2]
# 국,영,수 입력:98 45 23
# 총점: 166
# 평균: 55.3
# 등급: F

# 예3]
# 국,영,수 입력:89 97 100
# 총점: 286
# 평균: 95.3
# 등급: A

print('-' * 20)
kor, eng, math = map(int, input("국, 영, 수 입력 :").split())
total = kor + eng + math
avg = total / 3
print(f'총점 : {total}')
print(f'평균 : {avg:.1f}')

grade = None
if avg >= 90:
    grade = 'A'
elif avg >= 80:
    grade = 'B'
elif avg >= 70:
    grade = 'C'
elif avg >= 60:
    grade = 'D'
else:
    grade = 'F'
print(f'등급 : {grade}')

