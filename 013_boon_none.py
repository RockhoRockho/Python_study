# bool 타입
# True (참) 혹은 False (거짓) 값만 갖는 타입 

print(True)
print(False)
# print(true) # 에러

# 비교연산자, 논리연산자 의 결과는 bool 타입니다
# 비교연산자 : == (같다),  != (다르다),  >, >=, <, <=
# 논리연산자 : and, or, not

print(20 >= 10)
print(10 * 2 == 10)
print(10 * 2 != 10)
print(12.5 % 4.1 == 0.2)

print('-' * 20)

# None 타입
# 아무런 값도 없는 데이터

print(None)

# type() 함수
# type(타입을 알고자 하는 값/변수/식)
print(type(10))
print(type(10.0))
print(type("10.0"))
print(type(True))
print(type(None))

print(type(10 + 30))
print(type(4 // 2.0))
print(type(4 // 2))

# print() 는 여러개 데이터 출력가능
print(10, 20, 30)# print() 여러개 데이터 출력가능
                    # 콤마(,) 로 구분
                    # 각 출력 데이터 사이에 공백

# print(12 + "monkeys")
print(12, "monkeys")

print("10.0", type('10.0'))

